                cancelButtonColor: '#d33',
                confirmButtonText: 'Yes, delete it!'
            }).then((result) => {
                if (result.isConfirmed) {
                    $.ajax({
                        type: 'POST',
                        url: 'academic_list.php', 
                        data: $(form).serialize(),
                        success: function () {
                            Swal.fire({
                                icon: 'success',
                                title: 'Deleted!',
                                text: 'Academic year has been deleted.',
                                showConfirmButton: false,
                                timer: 2000
                            }).then(() => {
                                window.location.href = 'academic_list.php'; 
                            });
                        },
                        error: function () {
                            Swal.fire({
                                icon: 'error',
                                title: 'Oops...',
                                text: 'Failed to delete the academic year!',
                            });
                        }
                    });
                }
            });
        });
    });
</script>
<script>
    $(document).ready(function () {
        function updateTable(page = 1, search = '') {
            $.ajax({
                url: 'search_academic.php',
                method: 'POST',
                data: {
                    search: search,
                    page: page
                },
                success: function (response) {
                    var result = response.split('<!--pagination-->');

                    $('#list tbody').html(result[0]);

                    if (result.length > 1) {
                        $('#pagination-links').html(result[1]);
                    }
                },
                error: function () {
                    console.error('Failed to fetch search results.');
                }
            });
        }
        
        $('#searchInput').on('keyup', function () {
            let searchValue = $(this).val().trim();

            if (searchValue.startsWith('#')) {
                searchValue = searchValue.substring(1);
            }

            updateTable(1, searchValue);
        });

        $(document).on('click', '.pagination a', function (e) {
            e.preventDefault();
            let pageNumber = $(this).data('page');
            updateTable(pageNumber, $('#searchInput').val());
        });

        updateTable(1);
    });
</script>
